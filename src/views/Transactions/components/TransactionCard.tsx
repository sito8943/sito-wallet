import { useCallback, useEffect, useMemo, useRef, useState } from "react";
import type { MouseEvent } from "react";
import { useTranslation } from "react-i18next";
import { useDrag } from "@use-gesture/react";

// @sito/dashboard-app
import type { ActionType } from "@sito/dashboard-app";
import { classNames, useTimeAge } from "@sito/dashboard-app";

// icons
import {
  faCircle,
  faCircleCheck,
  faTrash,
} from "@fortawesome/free-solid-svg-icons";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";

// types
import type { TransactionCardPropsType } from "../types";

// views
import { Currency } from "views/Currencies/components/Currency";
import { Type } from "views/TransactionCategories/components/Type";

// lib
import {
  TransactionType,
  type TransactionDto,
  getPrimaryTransactionCategory,
  getTransactionCategories,
} from "lib";

// components
import { Autogenerated } from "views/TransactionCategories";
import {
  isMobileViewport,
  shouldPreventMobileContextMenu,
} from "../../../components/Card/utils";

// styles
import "./styles.css";

export function TransactionCard(props: TransactionCardPropsType) {
  const { t } = useTranslation();

  const {
    id,
    onClick,
    onSelect,
    onLongPress,
    selectionMode,
    selected,
    description,
    auto,
    date,
    amount,
    account,
    deletedAt,
    actions,
    swipeDeleteOpen = false,
    onSwipeDeleteTrigger,
  } = props;
  const deleted = !!deletedAt;
  const touchTimeoutRef = useRef<number | null>(null);
  const longPressTriggeredRef = useRef(false);
  const swipeShellRef = useRef<HTMLDivElement>(null);
  const swipeTriggeredRef = useRef(false);
  const swipeMovedRef = useRef(false);
  const [swipeOffset, setSwipeOffset] = useState(0);
  const [isSwiping, setIsSwiping] = useState(false);

  const { timeAge } = useTimeAge();

  const deleteAction = useMemo(
    () =>
      actions.find(
        (action) =>
          String(action.id).toLowerCase() === "delete" &&
          !action.hidden &&
          !action.disabled,
      ) as ActionType<TransactionDto> | undefined,
    [actions],
  );

  const swipeEnabled = useMemo(
    () =>
      !deleted &&
      !selectionMode &&
      !!deleteAction &&
      !swipeDeleteOpen &&
      isMobileViewport(),
    [deleteAction, deleted, selectionMode, swipeDeleteOpen],
  );

  const parsedDescription = useMemo(() => {
    if (!description?.length) return t("_entities:base.description.empty");
    if (auto)
      return `${t("_entities:transactionCategory.description.init")}: ${description}`;
    return description;
  }, [description, auto, t]);

  const transactionCategories = useMemo(
    () => getTransactionCategories(props),
    [props],
  );

  const primaryCategory = useMemo(
    () => getPrimaryTransactionCategory(props),
    [props],
  );

  const hasAutogeneratedCategory = useMemo(
    () => transactionCategories.some((category) => category.auto),
    [transactionCategories],
  );

  const parsedCategoryName = useMemo(() => {
    if (!transactionCategories.length) return "-";

    return transactionCategories
      .map((category) =>
        category.auto
          ? t("_entities:transactionCategory.name.init")
          : category.name,
      )
      .join(", ");
  }, [t, transactionCategories]);

  const clearTouchTimeout = useCallback(() => {
    if (touchTimeoutRef.current === null) return;
    window.clearTimeout(touchTimeoutRef.current);
    touchTimeoutRef.current = null;
  }, []);

  useEffect(() => {
    return () => {
      clearTouchTimeout();
    };
  }, [clearTouchTimeout]);

  const resetSwipeState = useCallback(() => {
    setSwipeOffset(0);
    setIsSwiping(false);

    window.setTimeout(() => {
      swipeMovedRef.current = false;
      swipeTriggeredRef.current = false;
    }, 0);
  }, []);

  const handleTouchStart = useCallback(() => {
    if (deleted || selectionMode || !onLongPress) return;

    clearTouchTimeout();
    touchTimeoutRef.current = window.setTimeout(() => {
      longPressTriggeredRef.current = true;
      onLongPress(id);
    }, 450);
  }, [clearTouchTimeout, deleted, id, onLongPress, selectionMode]);

  const handleClick = useCallback(() => {
    if (deleted) return;

    if (swipeMovedRef.current || swipeTriggeredRef.current) {
      resetSwipeState();
      return;
    }

    if (longPressTriggeredRef.current) {
      longPressTriggeredRef.current = false;
      return;
    }

    if (selectionMode) {
      onSelect?.(id);
      return;
    }

    onClick(id);
  }, [deleted, id, onClick, onSelect, resetSwipeState, selectionMode]);

  const handleContextMenu = useCallback(
    (event: MouseEvent<HTMLButtonElement>) => {
      if (!shouldPreventMobileContextMenu(!!onLongPress)) return;

      event.preventDefault();
    },
    [onLongPress],
  );

  const bindSwipe = useDrag(
    ({ down, movement: [movementX], last, cancel }) => {
      if (!swipeEnabled || !deleteAction) return;

      const shellWidth = swipeShellRef.current?.offsetWidth ?? 0;
      if (!shellWidth) return;

      const nextOffset = Math.max(0, Math.min(movementX, shellWidth));
      const reachedDeleteThreshold = nextOffset >= shellWidth * 0.5;

      if (nextOffset > 8) {
        clearTouchTimeout();
        swipeMovedRef.current = true;
      }

      if (down) {
        setIsSwiping(true);
        setSwipeOffset(nextOffset);

        if (reachedDeleteThreshold && !swipeTriggeredRef.current) {
          swipeTriggeredRef.current = true;
          setSwipeOffset(0);
          setIsSwiping(false);
          cancel();
          onSwipeDeleteTrigger?.(id);
          deleteAction.onClick(props);
          swipeMovedRef.current = false;
          swipeTriggeredRef.current = false;
        }

        return;
      }

      if (!reachedDeleteThreshold) {
        resetSwipeState();
        return;
      }

      if (last) {
        resetSwipeState();
      }
    },
    {
      axis: "x",
      filterTaps: true,
    },
  );

  return (
    <div ref={swipeShellRef} className="transaction-card-swipe-shell">
      {swipeEnabled || swipeDeleteOpen ? (
        <div
          className="transaction-card-swipe-delete"
          style={{ width: swipeDeleteOpen ? "50%" : `${swipeOffset}px` }}
          aria-hidden="true"
        >
          <FontAwesomeIcon
            icon={faTrash}
            className="transaction-card-swipe-delete-icon"
          />
        </div>
      ) : null}
      <button
        {...(swipeEnabled ? bindSwipe() : {})}
        name={
          selectionMode
            ? t("_accessibility:components.table.selectRow")
            : t("_pages:transactions.forms.edit")
        }
        aria-label={
          selectionMode
            ? t("_accessibility:components.table.selectRow")
            : t("_pages:transactions.forms.editAria")
        }
        onClick={handleClick}
        onTouchStart={handleTouchStart}
        onTouchEnd={clearTouchTimeout}
        onTouchCancel={clearTouchTimeout}
        onTouchMove={clearTouchTimeout}
        onContextMenu={handleContextMenu}
        className={classNames(
          "transition",
          "transaction-card",
          "transaction-card-swipe-foreground",
          deleted
            ? "transaction-card--deleted"
            : selectionMode
              ? selected
                ? "transaction-card--selected"
                : "transaction-card--selectable"
              : "transaction-card--idle",
        )}
        aria-pressed={selectionMode ? !!selected : undefined}
        style={{
          transform:
            swipeEnabled || swipeDeleteOpen
              ? swipeDeleteOpen
                ? "translateX(50%)"
                : `translateX(${swipeOffset}px)`
              : undefined,
          transitionDuration: isSwiping ? "0ms" : undefined,
          touchAction: swipeEnabled ? "pan-y" : undefined,
        }}
      >
        <Type
          className="transaction-card-type"
          type={primaryCategory?.type ?? TransactionType.In}
          filled={false}
          noText
        />
        <div className="transaction-card-body">
          <p
            className={classNames(
              "transaction-card-description",
              !description && "transaction-card-description--empty",
              deleted && "transaction-card-description--deleted",
            )}
          >
            {parsedDescription}
          </p>
          <div className="transaction-card-meta">
            {hasAutogeneratedCategory && <Autogenerated onlyIcon />}{" "}
            {parsedCategoryName}
            {date ? ` - ${timeAge(new Date(date))}` : ""}
          </div>
        </div>
        <p className="transaction-card-amount">
          {amount}{" "}
          <Currency
            name={account?.currency?.name}
            symbol={account?.currency?.symbol}
          />
        </p>
        {selectionMode && !deleted ? (
          <span
            className={classNames(
              selected
                ? "transaction-card-selection-indicator--selected"
                : "transaction-card-selection-indicator--idle",
            )}
          >
            <FontAwesomeIcon icon={selected ? faCircleCheck : faCircle} />
          </span>
        ) : null}
      </button>
    </div>
  );
}
