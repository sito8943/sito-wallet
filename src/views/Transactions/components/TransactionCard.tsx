import { useMemo } from "react";
import { useTranslation } from "react-i18next";

// @sito/dashboard-app
import { useTimeAge } from "@sito/dashboard-app";

// types
import { TransactionCardPropsType } from "../types";

// views
import { Currency } from "views/Currencies/components/Currency";
import { Type } from "views/TransactionCategories/components/Type";

// lib
import { TransactionType } from "lib";
import { Autogenerated } from "views/TransactionCategories";

export function TransactionCard(props: TransactionCardPropsType) {
  const { t } = useTranslation();

  const {
    id,
    onClick,
    description,
    auto,
    date,
    amount,
    category,
    account,
    deletedAt,
  } = props;
  const deleted = !!deletedAt;

  const { timeAge } = useTimeAge();

  const parsedDescription = useMemo(() => {
    if (!description?.length) return t("_entities:base.description.empty");
    if (auto)
      return `${t("_entities:transactionCategory.description.init")}: ${description}`;
    return description;
  }, [description, auto, t]);

  return (
    <button
      name={t("_pages:transactions.forms.edit")}
      aria-label={t("_pages:transactions.forms.editAria")}
      onClick={() => (!deleted ? onClick(id) : {})}
      className={`transition flex justify-between items-center gap-2 w-full min-w-0 overflow-hidden ${
        deleted
          ? "!border-bg-error border-2 opacity-60"
          : "bg-base hover:!bg-hover-primary hover:!text-white"
      } p-2 pr-3 rounded-lg`}
    >
      <Type
        className="bg-base-dark px-3 py-3.5 rounded-full"
        type={category?.type ?? TransactionType.In}
        filled={false}
        noText
      />
      <div className="flex-1 min-w-0 max-w-full flex flex-col justify-center items-start">
        <p
          className={`${description ? "" : "!text-xs italic"} text-start w-full overflow-hidden text-ellipsis whitespace-nowrap ${
            deleted ? "!text-bg-error" : ""
          }`}
        >
          {parsedDescription}
        </p>
        <div className="text-xs text-left flex gap-1">
          {category?.auto && <Autogenerated onlyIcon />} {category?.name} -{" "}
          {timeAge(new Date(date))}
        </div>
      </div>
      <p className="shrink-0">
        {amount}{" "}
        <Currency
          name={account?.currency?.name}
          symbol={account?.currency?.symbol}
        />
      </p>
    </button>
  );
}
