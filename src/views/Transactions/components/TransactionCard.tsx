import { useCallback, useEffect, useMemo, useRef } from "react";
import { useTranslation } from "react-i18next";

// @sito/dashboard-app
import { useTimeAge } from "@sito/dashboard-app";

// icons
import {
  faCircle,
  faCircleCheck,
} from "@fortawesome/free-solid-svg-icons";
import { FontAwesomeIcon } from "@fortawesome/react-fontawesome";

// types
import { TransactionCardPropsType } from "../types";

// views
import { Currency } from "views/Currencies/components/Currency";
import { Type } from "views/TransactionCategories/components/Type";

// lib
import { TransactionType } from "lib";
import { Autogenerated } from "views/TransactionCategories";

export function TransactionCard(props: TransactionCardPropsType) {
  const { t } = useTranslation();

  const {
    id,
    onClick,
    onSelect,
    onLongPress,
    selectionMode,
    selected,
    description,
    auto,
    date,
    amount,
    category,
    account,
    deletedAt,
  } = props;
  const deleted = !!deletedAt;
  const touchTimeoutRef = useRef<number | null>(null);
  const longPressTriggeredRef = useRef(false);

  const { timeAge } = useTimeAge();

  const parsedDescription = useMemo(() => {
    if (!description?.length) return t("_entities:base.description.empty");
    if (auto)
      return `${t("_entities:transactionCategory.description.init")}: ${description}`;
    return description;
  }, [description, auto, t]);

  const clearTouchTimeout = useCallback(() => {
    if (touchTimeoutRef.current === null) return;
    window.clearTimeout(touchTimeoutRef.current);
    touchTimeoutRef.current = null;
  }, []);

  useEffect(() => {
    return () => {
      clearTouchTimeout();
    };
  }, [clearTouchTimeout]);

  const handleTouchStart = useCallback(() => {
    if (deleted || selectionMode || !onLongPress) return;

    clearTouchTimeout();
    touchTimeoutRef.current = window.setTimeout(() => {
      longPressTriggeredRef.current = true;
      onLongPress(id);
    }, 450);
  }, [clearTouchTimeout, deleted, id, onLongPress, selectionMode]);

  const handleClick = useCallback(() => {
    if (deleted) return;

    if (longPressTriggeredRef.current) {
      longPressTriggeredRef.current = false;
      return;
    }

    if (selectionMode) {
      onSelect?.(id);
      return;
    }

    onClick(id);
  }, [deleted, id, onClick, onSelect, selectionMode]);

  return (
    <button
      name={
        selectionMode
          ? t("_accessibility:components.table.selectRow")
          : t("_pages:transactions.forms.edit")
      }
      aria-label={
        selectionMode
          ? t("_accessibility:components.table.selectRow")
          : t("_pages:transactions.forms.editAria")
      }
      onClick={handleClick}
      onTouchStart={handleTouchStart}
      onTouchEnd={clearTouchTimeout}
      onTouchCancel={clearTouchTimeout}
      onTouchMove={clearTouchTimeout}
      className={`transition relative flex justify-between items-center gap-2 w-full min-w-0 overflow-hidden ${
        deleted
          ? "!border-bg-error border-2 opacity-60"
          : selectionMode
            ? selected
              ? "!border-bg-primary border-2 bg-base"
              : "border-border border-1 bg-base"
          : "bg-base hover:!bg-hover-primary hover:!text-white"
      } p-2 pr-3 rounded-lg`}
      aria-pressed={selectionMode ? !!selected : undefined}
    >
      <Type
        className="bg-base-dark px-3 py-3.5 rounded-full"
        type={category?.type ?? TransactionType.In}
        filled={false}
        noText
      />
      <div className="flex-1 min-w-0 max-w-full flex flex-col justify-center items-start">
        <p
          className={`${description ? "" : "!text-xs italic"} text-start w-full overflow-hidden text-ellipsis whitespace-nowrap ${
            deleted ? "!text-bg-error" : ""
          }`}
        >
          {parsedDescription}
        </p>
        <div className="text-xs text-left flex gap-1">
          {category?.auto && <Autogenerated onlyIcon />} {category?.name} -{" "}
          {timeAge(new Date(date))}
        </div>
      </div>
      <p className="shrink-0">
        {amount}{" "}
        <Currency
          name={account?.currency?.name}
          symbol={account?.currency?.symbol}
        />
      </p>
      {selectionMode && !deleted && (
        <span className={`${selected ? "text-primary" : "text-text-muted"}`}>
          <FontAwesomeIcon icon={selected ? faCircleCheck : faCircle} />
        </span>
      )}
    </button>
  );
}
